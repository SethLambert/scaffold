"""Basic calculator module."""

def addIntegers(num1: int, num2: int) -> int:
    """Adds two numbers.
    
    Args:
        num1 (int) - first number to add
        num2 (int) - second number to add (to the first)

    Returns:
        int - num1 added to num 2
    """
    return num1 + num2

def subtractIntegers(num1: int, num2: int) -> int:
    """Subtracts num2 from num1.
    
    Args:
        num1 (int) - the number to subtract the value from 
        num2 (int) - the num to subtract (from num1)

    Returns:
        int - num1 minus num 2
    """
    return num1 - num2    